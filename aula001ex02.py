PI = 3.14                                                   #define o valor de PI
r = float(input('QUAL O RAIO DA CIRCUNFERENCIA: '))         #le o valor do raio e atribui a r
areaCirc = PI * r * r                                       #calcula a area do circulo
print('A AREA DA CIRCUNFERENCIA Ã‰ {}'.format(areaCirc))     #printa a area do circulo calculada
